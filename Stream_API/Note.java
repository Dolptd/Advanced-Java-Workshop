
// Stream API has many methods such as filter(), map(), reduce(), collect(),
// etc.

// filter() method is used to select elements as per the Predicate passed as
// argument.

// map() method is used to transform one object into other by applying a
// function.

// reduce() method is used to reduce the elements of a stream to a single value.

// collect() method is used to receive elements from a steam and store them in a
// collection.

// forEach() method is used to iterate through every element of the stream.

// flatMap() method is used to flatten a stream of collections to a stream of
// objects.

// sorted() method is used to sort the stream.

// distinct() method is used to remove duplicates from the stream.

// limit() method is used to reduce the size of the stream.

// skip() method is used to skip the elements from the stream.

// count() method is used to count the number of elements in the stream.

// anyMatch() method is used to check whether any element of the stream matches

// the given predicate.

// allMatch() method is used to check whether all the elements of the stream
// match the given predicate.

// noneMatch() method is used to check whether none of the elements of the
// stream match the given predicate.

// findFirst() method is used to find the first element of the stream.
